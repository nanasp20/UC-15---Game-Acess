    jLabel11.setText("Descrição: " + jogo.getDescricao());

    try {
        ImageIcon icon = new ImageIcon(getClass().getResource("/" + jogo.getImagem()));
        Image scaledImage = icon.getImage().getScaledInstance(200, 200, Image.SCALE_SMOOTH);
        lblImagem.setIcon(new ImageIcon(scaledImage));
    } catch (Exception e) {
        System.out.println("Erro: " + jogo.getImagem());
    }
